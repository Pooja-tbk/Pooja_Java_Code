package packageone;

public class DataTypeVar {

	public static void main(String[] args) {
		byte a1 = 10;// range 8 byte 2 to the power of(n-1), n=8
		System.out.println(a1);
		short a2 = 21345;// range 16 bytes
		System.out.println(a2);
		int a3 = 1345456;// range 32 bytes
		System.out.println(a3);
		long a4 = 12345678L;// 64 bytes
		System.out.println(a4);
		float a5 = 6.7865f;// 4 bytes
		System.out.println(a5);
		double a6 = 8.2436789;// 8 bytes
		System.out.println(a6);
		char grade = 'A';
		System.out.println(grade);
		String name = "Pooja";
		System.out.println(name);
		boolean res1 = true;
		System.out.println(res1);
		boolean res2 = false;
		System.out.println(res2);
	}

}
